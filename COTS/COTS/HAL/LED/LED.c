/******************************************************************************
* \Syntax          : void LED_SETVALUE(uint8 LED_NUM , uint8 LED_VALUE)
* \Description     : LED_SETVALUE
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void LED_SET_VALUE(uint8 LED_NUM , uint8 LED_VALUE)
{
	DIO_WRITE_CHANNEL(LED_NUM , LED_VALUE);
}

/******************************************************************************
* \Syntax          : void LED_Toggle(uint8 LED_NUM)
* \Description     : LED TOGGLE
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void LED_Toggle(uint8 LED_NUM)
{
	DIO_TOGGLE_CHANNEL(LED_NUM);
	
	_delay_ms(10);
}

